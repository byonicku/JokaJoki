    private void tabelPemesananMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelPemesananMouseClicked
        // TODO add your handling code here:
       int clickedRow = tabelPemesanan.getSelectedRow();
       TableModel tableModel = tabelPemesanan.getModel();
       
       selectedId = Integer.parseInt(tableModel.getValueAt(clickedRow, 9).toString());
       int penjokiId = Integer.parseInt(tableModel.getValueAt(clickedRow, 11).toString());
       
       if(penjokiId == 0)
           dropdownPenjoki.setEnabled(true);
       else
           dropdownPenjoki.setEnabled(false);
       
       String status = tableModel.getValueAt(clickedRow, 7).toString();
       
       dropdownPenjoki.removeAllItems();
       game = tableModel.getValueAt(clickedRow, 3).toString();
       
       List<Penjoki> listPenjokiSpecific = pc.getPenjokiSpecific(game);
       
       showPenjokiToDropdown(game);
       
       if(listPenjokiSpecific.isEmpty()){
           dropdownPenjoki.setEnabled(false);
           dropdownStatus.setEnabled(false);
           dropdownPenjoki.removeAllItems();
           dropdownPenjoki.addItem(new Penjoki(0, "< Tambah Penjoki Terlebih Dahulu >", "", 0));
           dropdownStatus.removeAllItems();
           dropdownStatus.addItem("< Tambah Penjoki Terlebih Dahulu >");
           return;
       }
       
       if(!status.equals("Pending")){
           int id_penjoki = Integer.parseInt(tableModel.getValueAt(clickedRow, 11).toString());
           int indexPenjoki = 0;
           
           for (Penjoki p : listPenjokiSpecific) 
                if(p.getId() == id_penjoki)
                    indexPenjoki = listPenjokiSpecific.indexOf(p);
           
           dropdownPenjoki.setSelectedIndex(indexPenjoki);
       }
       
       if(status.equals("Pending")){
            dropdownPenjoki.setSelectedIndex(0);
            dropdownStatus.setSelectedIndex(0);
            dropdownPenjoki.setEnabled(true);
            dropdownStatus.setEnabled(true);
            dropdownStatus.removeAllItems();
            saveBtn.setEnabled(true);
            setDataDropdown();
       }
       else if(status.equals("On-going")){
           dropdownPenjoki.setEnabled(false);
           dropdownStatus.setEnabled(true);
           dropdownStatus.removeAllItems();
           saveBtn.setEnabled(true);
           setDataDropdown();
       }
       else if(status.equals("Completed")){
           dropdownPenjoki.setEnabled(false);
           dropdownStatus.setEnabled(false);
           dropdownStatus.removeAllItems();
           dropdownStatus.addItem("< Pemesanan Sudah Selesai >");
           saveBtn.setEnabled(false);
       }
    }//GEN-LAST:event_tabelPemesananMouseClicked

