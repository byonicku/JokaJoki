    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        List<Pemesanan> listPemesanan = peC.getPemesananAdmin("");
        
        Pemesanan p = null;
        
        for (int i = 0; i < listPemesanan.size(); i++) {
            if(listPemesanan.get(i).getId() == selectedId) {
                p = listPemesanan.get(i);
                break;
            }
        }
 
        if(p.getStatus_pemesanan().equals("Pending")){
            List<Penjoki> listPenjokiSpecific = pc.getPenjokiSpecific(game);
            Penjoki joki = listPenjokiSpecific.get(dropdownPenjoki.getSelectedIndex());
            p.setPenjoki(joki);
        }

        p.setStatus_pemesanan(dropdownStatus.getSelectedItem().toString());
        
        peC.updatePenjokiStatus(p);
        inputSearch.setText("");
        showPemesananAdminPendingOngoing();
        setComponent(false);
        clearDropdown();
        dropdownPenjoki.setSelectedIndex(0);
        dropdownStatus.setSelectedIndex(0);
    }//GEN-LAST:event_saveBtnActionPerformed

